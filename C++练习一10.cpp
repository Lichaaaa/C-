#include<iostream>
using namespace std;
int main()
{
	//转义字符
	//换行符\n
	cout << "hello world\n";
	//反斜杠 \\
	//目的是单纯的输出一个反斜杠;\
	cout <<"\\"<<end1;
	//水平制表符 \t
	cout << "a\thello world\n";
	cout << "aa\thello world\n";
	cout << "aaa\thello world\n";
	//a hello world
	//aa hello world
	//aaa hello world
	system("pause");
}